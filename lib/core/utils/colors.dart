import 'package:flutter/material.dart';

class AppColors {
  static Color orange(alpha) => Color(0xFFFF7024).withValues(alpha: alpha);
  static Color purple(alpha) => Color(0xFFB865E9).withValues(alpha: alpha);
  static Color blue(alpha) => Color(0xFF2337ED).withValues(alpha: alpha);
  static Color coal(alpha) => Color(0xFF231F1E).withValues(alpha: alpha);
  static Color coalLight(alpha) => Color(0xFF35302E).withValues(alpha: alpha);
  static Color rice(alpha) => Color(0xFFF5DFD2).withValues(alpha: alpha);
  static Color white(alpha) => Color(0xFFFFFFFF).withValues(alpha: alpha);
  static Color black(alpha) => Color(0xFF000000).withValues(alpha: alpha);
  static Color red(alpha) => Color(0xFFE32A2A).withValues(alpha: alpha);

  // Gradient Colors
  static Color gradientDarkBlue(alpha) =>
      Color(0xFF00033D).withValues(alpha: alpha);
  static Color gradientBlue(alpha) =>
      Color(0xFF0033FF).withValues(alpha: alpha);
  static Color gradientPink(alpha) =>
      Color(0xFF977DFF).withValues(alpha: alpha);
}
